<p class="no-save">Great News! Selections, Sorting, and Deletions happen in real time! You don't have to click the Update button!</p>
<div class="controls">
  <h4>Select and Upload Images</h4>
  <p>When you click the "Select Images" button, the standard Wordpress Media Uploader is launched.</p>
  <p>Then you can easily select any number of images to add to the post. It works just like the standard "Media" button.</p>
  <p>Add Unlimited amount of Images!</p>
</div>
<div class="controls">
  <h4>Sorting Images</h4>
  <p>Sorting images is simple as well. Once you have your images uploaded, you can arrange them in any way you need to.</p>
  <p><b><em>The Image furthest to the Left is the 1st image that will be displayed on the shortcode output.</em></b></p>
</div>
<div class="controls">
  <h4>Deleting Images</h4>
  <p>To delete one images or multiple images simply double click the image (hove in the middle). You will see a red border around the image itself.</p>
  <p>Once you have made your selection(s) you can click the "Delete Images" button to remove them from the view and the front-end display. <u>The images themselves are not removed from the server</u>.</p>
</div>
<div class="controls caveats">
  <h4>Suggestions and Caveats</h4>
  <div>
    <ul>
      <li><strong>Sorting</strong>: Please let your images snap into place. After you click on an image to move it, you will want to move the hand in-between the 2 other images where you want to place it. It all has to do with where you click the image to drag it.</li>
      <li><strong>Deleting</strong>: Check your sorting order after deleting multiple images. Seems a bit 'iffy'</li>
    </ul>
  </div>
</div>
